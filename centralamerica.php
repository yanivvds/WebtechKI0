                    </li>
                    <li>
                        <strong>Antigua, Guatemala:</strong> Step into the colonial beauty of Antigua, Guatemala. This UNESCO World Heritage Site features well-preserved Spanish Baroque-style architecture, cobbled streets and lively markets. It is a cultural center surrounded by volcanoes.
                    </li>
                    <li>
                        <strong>Manuel Antonio National Park, Costa Rica:</strong> Discover the biodiversity of Manuel Antonio National Park in Costa Rica. This coastal gem combines tropical rainforests with pristine beaches. Encounter diverse wildlife, including monkeys and sloths, as you stroll the park's trails.
